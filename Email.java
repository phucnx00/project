package ban_quan_ao;

public class Email {
	private String email;
	private  String message;
	public Email(String email) {
		this.email = email;
	}

	public String getEmail() {
		return email;
	}

	

	public  String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}
	
	
}
